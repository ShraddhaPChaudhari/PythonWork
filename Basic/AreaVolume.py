#accepy lenght,breadth,height from the user and print its area and volume.
l=int(input('enter lenght of a box'))
b=int(input('enter breadth of a box'))
h=int(input('enter breadth of a box'))
area=2*l*b+2*b*h+2*h*l
volume=l*b*h
print('area of the box is',area)
print('volume of the box is',volume)
